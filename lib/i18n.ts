export type Language = keyof typeof translations
export type TranslationKey = keyof typeof translations.en

export function getTranslation(lang: Language, key: TranslationKey): string {
  return translations[lang]?.[key] || translations.en[key] || key
}

export function getNestedTranslation(lang: Language, category: string, key: string): string {
  const categoryTranslations = (translations[lang] as any)?.[category] || (translations.en as any)?.[category]
  return categoryTranslations?.[key] || key
}
